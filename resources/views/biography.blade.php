      La compagnie de danse Dancefloor est née à Genève en mars 2009 avec la rencontre inopinée entre Fred ( France) et BoogTom (Corée du Sud). A cette époque, BoogTom est un danseur professionnel de hip hop qui participe ou juge différents concours (battles) à travers le monde. Cette rencontre a été rendue possible grâce à Junior Boogaloo, l’un des danseurs hip hop les plus connus et reconnus mondialement. Après un repas chez Junior, Fred emmène Tom en soirée salsa et après quelques échanges de pas libres sur la salsa, Fred propose à BoogTom de former un duo dont la particularité serait de se fusionner la salsa et le hip hop.
