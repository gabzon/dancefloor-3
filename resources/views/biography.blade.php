      <h2>&nbsp;</h2>
      Dancefloor was founded in Geneva in March 2009. The company’s story began with an unexpected encounter between Fred (France) and BoogTom (South Korea). This meeting was facilitated by one of the world’s most recognized hip-hop dancers, Junior Boogaloo. After a friendly dinner at Junior’s house, Fred brought Boog Tom—also a world-renouned professional hip-hop dancer who participates and judges international competitions and battles—out to a salsa party in Geneva. After a fruitful excahnge of their different dance styles, Fred suggested forming a duo with the aim of integrating both salsa and hip-hop movements.
      <br><br>
      The immediate goal of the duo was to showcase their innovative fusion of the two dance styles in a performance entitled “Krush la 33.” This show took place in May 2009 at Salsa Vintage—a salsa festival held in Geneva. The the originality and creativity of their performance brought them public recognition by the event organizer, only two months after their initial meeting. This recognition quickly led to numerous perfomance invitations, the most important being Salsorro (Spain).
