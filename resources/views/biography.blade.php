    <div class="col-12 col-sm-6 col-md-4 col-lg-4 i text-muted">
      In 2010 Fred visited BoogTom for one month in South Korea. During his time there he was exposed to South Koreans’ discipline and strong work ethic, as well the manner in which BoogTom created various choreographies. That year they decided to collaborate on their second choreography “Moments in Boogaloo.” The show was performed for the first time at the Copa Loca salsa festival in Besançon (France). Committed to providing quality shows, they both worked hard: BoogTom on cha cha cha and Fred on controlled body movement. Moments in Boogaloo was a huge success, and paved the way to many subsequent festival invitations.
